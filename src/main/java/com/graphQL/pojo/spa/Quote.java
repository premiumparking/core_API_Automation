package com.graphQL.pojo.spa;

public class Quote {

	String address, city, email, facilityType, name, phone, spaceCount, startDate;
	Integer floors;
	Boolean gates, newConstruction;

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getFacilityType() {
		return facilityType;
	}

	public void setFacilityType(String facilityType) {
		this.facilityType = facilityType;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getSpaceCount() {
		return spaceCount;
	}

	public void setSpaceCount(String spaceCount) {
		this.spaceCount = spaceCount;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public Integer getFloors() {
		return floors;
	}

	public void setFloors(Integer floors) {
		this.floors = floors;
	}

	public Boolean getGates() {
		return gates;
	}

	public void setGates(Boolean gates) {
		this.gates = gates;
	}

	public Boolean getNewConstruction() {
		return newConstruction;
	}

	public void setNewConstruction(Boolean newConstruction) {
		this.newConstruction = newConstruction;
	}

}
